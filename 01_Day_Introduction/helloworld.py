# Checking data types


print(type(10))                    
# Int - •	whole numbers, •	no decimal point, •	used for counting, indexing and calculations 
print(type(3.14))                  
# Float - •	numbers with decimal points , •	used for measurements, money and averages
print(type(1 + 3j))                
# Complex - •	numbers with real and imaginary parts, •	written as a + bj, •	j represents √-1, •	used in advanced math, physics and engineering 
print(type('Asabeneh'))            
# String - •	text data, •	written inside single ‘’ or double “” quotes 
print(type([1, 2, 3]))            
# List - •	stores multiple values, •	ordered and changeable (mutable), •	written using [], •	used to store items like number, names, etc 
print(type({'name': 'Asabeneh'})) 
# Dictionary - •	stores data as a key : value pairs, •	written using {}, •	used to store structured data  
print(type({9.8, 3.14, 2.7}))     
# Set - •	stores unique values only, •	unordered, •	written in {} without key : value pairs, •	used when you don’t want duplicate values 
print(type((9.8, 3.14, 2.7)))      
